import authService from "../services/authService.js";
import { createJWT } from "../utils/token.js";

const signup = async (req, res) => {
  try {
    const input = req.body;
    if (!input.password || !input.confirmPassword)
      return res.status(400).json({ message: "Password and Confirm Password are required" });

    if (input.password !== input.confirmPassword)
      return res.status(400).json({ message: "Passwords do not match" });

    const user = await authService.signup(input);

    res.status(201).json({
      message: "User registered successfully. Check your email for verification code.",
      user,
    });
  } catch (error) {
    res.status(error.statusCode || 500).json({ message: error.message || "Server error" });
  }
};

const verifyEmail = async (req, res) => {
  try {
    const { email, verificationCode } = req.body;
    const result = await authService.verifyEmail(email, verificationCode);

    res.status(200).json({ success: true, message: result.message });
  } catch (error) {
    res.status(error.statusCode || 500).json({ success: false, message: error.message || "Server error" });
  }
};

const resendOtp = async (req, res) => {
  try {
    const { email } = req.body;
    const result = await authService.resendOtp(email);

    res.status(200).json({ success: true, message: result.message });
  } catch (error) {
    res.status(error.statusCode || 500).json({ success: false, message: error.message || "Server error" });
  }
};

const login = async (req, res) => {
  try {
    const input = req.body;
    const user = await authService.login(input);
    const authToken = createJWT(user);

    res.cookie("authToken", authToken, {
      httpOnly: true,
      sameSite: "strict",
      maxAge: 24 * 60 * 60 * 1000,
    });

    res.status(200).json({ message: "Login successful", user });
  } catch (error) {
    res.status(error.statusCode || 500).json({ message: error.message || "Server error" });
  }
};

const forgotPassword = async (req, res) => {
  try {
    const { email } = req.body;
    const result = await authService.forgotPassword(email);

    res.status(200).json({ success: true, message: result.message });
  } catch (error) {
    res
      .status(error.statusCode || 500)
      .json({ success: false, message: error.message || "Server error" });
  }
};

const resetPassword = async (req, res) => {
  try {
    const { email, code, newPassword, confirmPassword } = req.body;

    if (!newPassword || !confirmPassword)
      return res
        .status(400)
        .json({ message: "New Password and Confirm Password are required" });

    if (newPassword !== confirmPassword)
      return res.status(400).json({ message: "Passwords do not match" });

    const result = await authService.resetPassword(
      email,
      code,
      newPassword
    );

    res.status(200).json({ success: true, message: result.message });
  } catch (error) {
    res
      .status(error.statusCode || 500)
      .json({ success: false, message: error.message || "Server error" });
  }
};

const logout = async (req, res) => {
  try {
    res.clearCookie("authToken", { httpOnly: true, sameSite: "strict" });
    res.status(200).json({ message: "Logout successful" });
  } catch (error) {
    res.status(500).json({ message: "Logout failed" });
  }
};

export default { signup, verifyEmail, login, logout, resendOtp, forgotPassword, resetPassword };
